class Student {
  final String name;
  final String year;
  final String roll;

  Student({this.name, this.year, this.roll});
}

class Teacher {
  final String name;
  final String dept;
  final String sub;
  final String describe;

  Teacher({this.name, this.dept, this.sub, this.describe});
}

class Notice {
  final String sub;
  final String describe;
  final String time;

  Notice({this.sub, this.describe, this.time});
}
